// ─────── Fleet enrichment (status / battery / firmware / LTE) ───────

function mulberry32(seed) {
  return function () {
    let t = (seed += 0x6d2b79f5);
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

const LTE_CARRIERS = ['T-Mobile IoT', 'Verizon ThingSpace', 'AT&T IoT'];
const LTE_BANDS = ['B2 (1900 MHz)', 'B4 (AWS-1)', 'B12 (700 MHz)', 'B13 (700 MHz)'];

function enrichDevice(raw, seed, type) {
  const rng = mulberry32(seed);
  const ri = (min, max) => Math.floor(min + rng() * (max - min));
  const pick = (arr) => arr[ri(0, arr.length)];

  // Status roll — higher offline rate than bank fleet because HQ2 has a
  // couple of documented flaky units from field notes.
  const roll = rng();
  const status = roll < 0.02 ? 'offline' : roll < 0.06 ? 'degraded' : roll < 0.09 ? 'updating' : 'online';
  const online = status !== 'offline';

  const battery = status === 'offline' ? ri(0, 12) : ri(22, 100);
  const daysPerPct = ((type === 'display_sdg' ? 60 : 36) * 30) / 100; // 5y SDG, 3y PC
  const batteryDays = Math.round(battery * daysPerPct);

  const fwStable = type === 'display_sdg' ? '3.8.10' : '2.4.1';
  const fwRolling = type === 'display_sdg' ? '3.9.0-rc1' : '2.5.0-rc2';
  const fw = status === 'updating' ? fwRolling : fwStable;

  const site = IMF_SITES.find((s) => s.short === raw.building) || IMF_SITES[0];
  const floorLabel = raw.floor || '?';
  const aisle = raw.aisle || '';
  const room = `${raw.room_type || 'RR'}${raw.room_number ? ' #' + raw.room_number : ''}`;

  return {
    id: raw.sn,
    type,
    status,
    site_id: site.id,
    site_name: site.name,
    building: raw.building,
    zone: raw.building === 'HQ1' ? 'hq1' : 'hq2',
    aisle,
    floor: floorLabel,
    room,
    room_type: raw.room_type,
    location: `${raw.building} · Fl ${floorLabel}${aisle ? ' · ' + aisle : ''} · ${room}`,
    uplink: 'LTE',
    lte: {
      carrier: pick(LTE_CARRIERS),
      tech: 'LTE-M (Cat-M1)',
      band: pick(LTE_BANDS),
      imei: raw.imei,
      iccid: raw.iccid,
      rsrp: online ? -(55 + ri(0, 35)) : null,
      rsrq: online ? -(6 + ri(0, 12)) : null,
      sinr: status === 'degraded' ? ri(-3, 6) : online ? ri(8, 22) : null,
      apn: 'iot.adaptivsystems.net',
      data_mb_mtd: +(2 + rng() * 30).toFixed(1),
      online,
    },
    rssi: online ? -(55 + ri(0, 35)) : null,
    battery,
    battery_days_remaining: batteryDays,
    battery_chemistry:
      type === 'display_sdg' ? 'Li-SOCl\u2082 D-cell pack \u00b7 swappable' : 'Li-SOCl\u2082 AA pack \u00b7 swappable',
    battery_swappable: true,
    firmware: fw,
    fw_latest: fwRolling,
    fw_behind: fw !== fwRolling,
    fw_updating: status === 'updating',
    fw_progress: status === 'updating' ? ri(15, 85) : null,
    uptime: +(
      status === 'online'
        ? 97 + rng() * 3
        : status === 'degraded'
          ? 85 + rng() * 10
          : status === 'offline'
            ? 40 + rng() * 30
            : 92 + rng() * 7
    ).toFixed(1),
    last_packet_s: status === 'offline' ? ri(3600, 48 * 3600) : ri(5, 240),
    install_date: '2025-04-04',
    display_tech: type === 'display_sdg' ? '7" passive eInk \u00b7 low-power signage' : null,
    embedded:
      type === 'display_sdg'
        ? {
            nfc: online,
            temp_c: online ? +(20 + rng() * 4).toFixed(1) : null,
            light_lux: online ? ri(180, 620) : null,
            accel_g: online ? +(0.02 + rng() * 0.03).toFixed(2) : null,
            buttons: 0,
          }
        : { pir: online, mmwave: online, count_today: online ? ri(40, 420) : 0 },
    error:
      status === 'offline'
        ? { code: 'E-042', msg: 'No LTE heartbeat \u00b7 battery check required' }
        : status === 'degraded'
          ? pick([
              { code: 'W-017', msg: 'LTE packet loss 12% \u00b7 weak signal' },
              { code: 'W-022', msg: 'Firmware update stalled' },
              { code: 'W-008', msg: 'Battery trending below 20%' },
            ])
          : null,
  };
}

